# Функція len()
my_list = [1, 2, 3, 4, 5]
length_of_list = len(my_list)
print("Довжина списку:", length_of_list)

# Функція sum()
numbers = [1, 2, 3, 4, 5]
sum_of_numbers = sum(numbers)
print("Сума чисел:", sum_of_numbers)

# Функція max() та min()
values = [10, 7, 25, 13, 6]
max_value = max(values)
min_value = min(values)
print("Найбільше значення у списку:", max_value)
print("Найменше значення у списку:", min_value)

