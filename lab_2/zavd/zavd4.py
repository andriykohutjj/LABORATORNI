# Цикл for
my_list = [1, 2, 3, 4, 5]
print("Цикл for для перебору списку:")
for number in my_list:
    print(number)

# Цикл while 
counter = 0
print("Цикл while для підрахунку:")
while counter < 5:
    print(counter)
    counter += 1

# Використання циклу for з функцією range()
print("Цикл for з функцією range():")
for i in range(1, 6):  # Послідовність від 1 до 5
    print(i)


