# Приклад з введенням числа і його конвертацією у ціле число

try:
    user_input = input("Введіть число: ")
    number = int(user_input)
    print("Введене число:", number)

except ValueError:
    print("Помилка! Введене значення не є числом.")

finally:
    print("Ця частина завжди виконується, навіть якщо сталася помилка або не сталася.")
